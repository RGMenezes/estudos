/* Null

  - Ausência intencional de valor. 
  - Variável não inicializada.
  - Aceita apenas um valor.
  - Tipo null só atribui null.
*/

/* Undefined

 - Variaveis não inicializadas.
 - Tem apenas um valor. 
 - Função que não retorna valor.
*/

//Null não é igual a Undefined.


let variavelTeste = null;

console.log(variavelTeste);
console.log(typeof variavelTeste);


let variavelTeste2;

console.log(variavelTeste2);
console.log(typeof variavelTeste2);


